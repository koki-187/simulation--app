/**
 * 借入額に応じた事務手数料を計算
 */
export function calcProcessingFee(bank: RefinanceBank2026, loanAmount: number): number {
  if (bank.processingFeeType === 'percent') {
    return loanAmount * bank.processingFeeValue;
  }
  return bank.processingFeeValue;
}
